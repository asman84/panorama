function router(){
  switch($_POST['action']) {
    case 'comment':
    getComment();
    break;
    case 'movies':
    getMovies();
    break;
  }
}

function filterComment($comment) {
  return $comment['id'] == $_POST['id'];
}

function filterMovie($movie) {
  if (isset($_POST['type'])) {
    return $movie['type'] === $_POST['type'];
  } else {
    return true;
  }
}
